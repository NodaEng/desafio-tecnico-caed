CREATE TABLE StudentClass (
    id INT PRIMARY KEY,
    code VARCHAR(8)
);

CREATE TABLE Student (
    id INT PRIMARY KEY,
    class_id INT,
    full_name VARCHAR(64),

    FOREIGN KEY (class_id) REFERENCES StudentClass(id)
);

CREATE TABLE Course (
    id INT PRIMARY KEY,
    title VARCHAR(32)
);

CREATE TABLE Enrollment (
    id INT PRIMARY KEY,
    student_id INT,
    course_id INT,
    total_grade FLOAT,

    FOREIGN KEY (student_id) REFERENCES Student(id),
    FOREIGN KEY (course_id) REFERENCES Course(id)
);

CREATE TABLE Activity (
    id INT PRIMARY KEY,
    subject_id INT,
    title VARCHAR(32),
    grade_weight INT,

    FOREIGN KEY (course_id) REFERENCES Course(id)
);

CREATE TABLE Grade (
    id INT PRIMARY KEY,
    student_id INT,
    activity_id INT,
    result FLOAT,

    FOREIGN KEY (student_id) REFERENCES Student(id),
    FOREIGN KEY (activity_id) REFERENCES Activity(id)
);

